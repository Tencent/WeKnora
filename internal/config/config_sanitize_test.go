func TestSanitizeUnresolvedPlaceholders_ClearsLiteralEnvTokens(t *testing.T) {
	cfg := &Config{
		ParserDefaults: &ParserDefaultsConfig{
			MinerU: &MinerUDefaults{
				Endpoint:     "${MINERU_ENDPOINT}",
				Model:        "pipeline",
				VLMServerURL: "${MINERU_VLM_SERVER_URL}",
				Language:     "",
			},
			MinerUCloud: &MinerUCloudDefaults{
				APIKey:   "${MINERU_API_KEY}",
				Model:    "vlm",
				Language: "${MINERU_CLOUD_LANGUAGE}",
			},
		},
		StorageDefaults: &StorageDefaultsConfig{
			DefaultProvider: "${STORAGE_DEFAULT_PROVIDER}",
			MinIO: &types.MinIOEngineConfig{
				Endpoint:        "${MINIO_ENDPOINT}",
				AccessKeyID:     "minioadmin",
				SecretAccessKey: "${MINIO_SECRET_ACCESS_KEY}",
				BucketName:      "weknora-bucket",
			},
			S3: &types.S3EngineConfig{
				Region:    "${S3_REGION}",
				AccessKey: "AKIA...",
			},
		},
	}

	sanitizeUnresolvedPlaceholders(cfg)

	// Parser defaults: literal placeholders should be cleared, real values kept.
	if got := cfg.ParserDefaults.MinerU.Endpoint; got != "" {
		t.Errorf("MinerU.Endpoint = %q, want cleared", got)
	}
	if got := cfg.ParserDefaults.MinerU.Model; got != "pipeline" {
		t.Errorf("MinerU.Model = %q, want preserved real value 'pipeline'", got)
	}
	if got := cfg.ParserDefaults.MinerU.VLMServerURL; got != "" {
		t.Errorf("MinerU.VLMServerURL = %q, want cleared", got)
	}
	if got := cfg.ParserDefaults.MinerUCloud.APIKey; got != "" {
		t.Errorf("MinerUCloud.APIKey = %q, want cleared", got)
	}
	if got := cfg.ParserDefaults.MinerUCloud.Model; got != "vlm" {
		t.Errorf("MinerUCloud.Model = %q, want preserved", got)
	}
	if got := cfg.ParserDefaults.MinerUCloud.Language; got != "" {
		t.Errorf("MinerUCloud.Language = %q, want cleared", got)
	}

	// Storage defaults: same treatment.
	if got := cfg.StorageDefaults.DefaultProvider; got != "" {
		t.Errorf("DefaultProvider = %q, want cleared", got)
	}
	if got := cfg.StorageDefaults.MinIO.Endpoint; got != "" {
		t.Errorf("MinIO.Endpoint = %q, want cleared", got)
	}
	if got := cfg.StorageDefaults.MinIO.AccessKeyID; got != "minioadmin" {
		t.Errorf("MinIO.AccessKeyID = %q, want preserved", got)
	}
	if got := cfg.StorageDefaults.MinIO.SecretAccessKey; got != "" {
		t.Errorf("MinIO.SecretAccessKey = %q, want cleared", got)
	}
	if got := cfg.StorageDefaults.MinIO.BucketName; got != "weknora-bucket" {
		t.Errorf("MinIO.BucketName = %q, want preserved", got)
	}
	if got := cfg.StorageDefaults.S3.Region; got != "" {
		t.Errorf("S3.Region = %q, want cleared", got)
	}
	if got := cfg.StorageDefaults.S3.AccessKey; got != "AKIA..." {
		t.Errorf("S3.AccessKey = %q, want preserved", got)
	}
}

