func TestUnresolvedPlaceholderPattern_OnlyMatchesEnvLiterals(t *testing.T) {
	tests := []struct {
		in   string
		want bool
	}{
		{"${FOO}", true},
		{"${MINERU_ENDPOINT}", true},
		{"${ABC_123_DEF}", true},
		{"", false},
		{"plain value", false},
		{"prefix${FOO}", false},          // partial substitution is not our concern
		{"${foo}", false},                // lowercase isn't generated by our pattern
		{"http://${FOO}/bar", false},
		{"$FOO", false},
	}
	for _, tc := range tests {
		if got := unresolvedPlaceholderPattern.MatchString(tc.in); got != tc.want {
			t.Errorf("pattern.MatchString(%q) = %v, want %v", tc.in, got, tc.want)
		}
	}
}

func TestSnapshot_StoreAndLoad(t *testing.T) {
	// Capture previous value to restore for test isolation.
	prev := snapshotPtr.Load()
	t.Cleanup(func() {
		if prev == nil {
			snapshotPtr.Store(nil)
		} else {
			snapshotPtr.Store(prev)
		}
	})

	cfg := &Config{
		StorageDefaults: &StorageDefaultsConfig{DefaultProvider: "minio"},
	}
	storeSnapshot(cfg)
	got := Snapshot()
	if got != cfg {
		t.Fatalf("Snapshot() returned %p, want %p", got, cfg)
	}
	if got.StorageDefaults.DefaultProvider != "minio" {
		t.Errorf("Snapshot.StorageDefaults.DefaultProvider = %q, want minio",
			got.StorageDefaults.DefaultProvider)
	}
}

func TestSnapshot_NilStoreIgnored(t *testing.T) {
	prev := snapshotPtr.Load()
	t.Cleanup(func() {
		if prev == nil {
			snapshotPtr.Store(nil)
		} else {
			snapshotPtr.Store(prev)
		}
	})

	sentinel := &Config{}
	snapshotPtr.Store(sentinel)
	storeSnapshot(nil)
	if got := Snapshot(); got != sentinel {
		t.Errorf("storeSnapshot(nil) clobbered previous snapshot")
	}
}
